                </p>
                <h2>
                    1. Latihan berat badan
                </h2>
                <p>
                    Salah satu cara paling mudah dan efektif adalah melalui latihan berat badan. Ini adalah latihan yang menggunakan berat badan sebagai perlawanan seperti push up, squat, pull up, dan plank. Sahabat Fimela bisa melakukan latihan ini di mana saja, seperti di kamar hotel. Latihan ini tidak hanya bagus untuk membangun kekuatan dan daya tahan, tetapi juga mudah dimodifikasi berdasarkan tingkat kebugaran dan ruang yang tersedia.
                </p>
                <h2>
                    2. Yoga
                </h2>
                <p>
                    Yoga adalah cara terbaik untuk meregangkan, memperkuat, dan merilekskan tubuh Sahabat Fimela, menjadikannya aktivitas yang sempurna untuk liburan. Baik kamu seorang pemula atau yoga berpengalaman, ada banyak rutinitas dan kelas yoga yang tersedia secara online atau tatap muka. Sahabat Fimela bisa melakukan yoga di taman, atau bahkan di kamar hotel, bahkan di pantai. Yoga membutuhkan peralatan minimal, menjadikannya latihan yang mudah untuk dimasukkan ke dalam liburanmu.
                </p>
                <h2>
                    3. Berenang
                </h2>
                <p>
                    Berenang adalah olahraga seluruh tubuh yang ringan untuk persendian, menjadikannya pilihan olahraga yang sangat baik bagi mereka yang sedang berlibur. Baik Sahabat Fimela berenang di laut atau di kolam renang, berenang memberikan latihan kardiovaskular yang baik sekaligus melatih otot. Kamu dapat melakukan gerakan renang yang berbeda untuk menargetkan kelompok otot yang berbeda dan membuat renang lebih menantang. Jika Sahabat Fimela tidak yakin dengan kemampuan berenang, pertimbangkan untuk mengambil satu atau dua pelajaran untuk meningkatkan teknik berenang.
                </p>
                <h2>
                    4. Olahraga di luar ruangan
                </h2>
                <p>
                    Banyak destinasi liburan yang menawarkan kesempatan untuk bermain olahraga luar ruangan seperti bola voli, bola basket, atau sepak bola. Olahraga ini memberikan cara yang menyenangkan secara sosial untuk berolahraga saat berlibur. Olahraga di luar ruangan tidak hanya menawarkan latihan kardiovaskular yang baik, tetapi juga meningkatkan koordinasi tangan, mata, dan keterampilan kerja tim.
                </p>
                <h2>
                    5. High-Intensity Interval Training (HIIT)
                </h2>
                <p>
                    HIIT atau High Intensity Interval Training adalah olahraga yang menggabungkan jenis latihan intensitas tinggi dengan waktu pemulihan. Latihan HIIT menjadi cara terbaik untuk berolahraga dengan cepat dan efisien saat liburan. HIIT melibatkan latihan singkat dengan intensitas tinggi diikuti dengan waktu istirahat singkat. Latihan ini dapat dilakukan hanya dalam 15-20 menit dan dapat dimodifikasi berdasarkan tingkat kebugaran Sahabat Fimela. HIIT sangat bagus untuk membakar kalori dan meningkatkan metabolisme tubuh, menjadikannya latihan yang sempurna untuk liburan saat kamu kekurangan waktu.
                </p>
