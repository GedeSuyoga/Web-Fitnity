            <h1>10 Cara Efektif Memperbesar Otot Lengan Tanpa Harus ke Gym</h1>
            <div class="imga">
                <img src="{{ asset('img/tips/ototlengan.png') }}" alt="">
                <div class="sources">
                    <p>
                        https://www.idntimes.com/health/fitness/m-tarmizi-murdianto/cara-membesarkan-otot-lengan-tanpa-pergi-ke-gym?page=all
                    </p>
                </div>
            </div>
            <div class="content">
                <p>
                  Memiliki lengan yang berotot dan kuat merupakan impian banyak orang. Pasalnya, otot lengan yang kuat dan berbentuk akan membuat tubuh terlihat lebih gagah dan kuat.
                </p>
                <p>
                  Tidak hanya itu, otot lengan yang kuat juga akan memudahkan kamu melakukan aktivitas sehari-hari seperti menenteng belanjaan, menggendong anak, atau mengangkut keranjang pakaian. 
                </p>
                <p>
                  Berikut ini cara efektif memperbesar otot lengan di rumah tanpa harus ke gym. Dengan catatan, lakukan semuanya secara konsisten.
                </p>
                <h2>
                  1. Elevated pike push-up
                </h2>
                <p>
                  Gerakan yang satu ini membutuhkan kursi atau boks yang kokoh untuk menempatkan kaki dan melakukan posisi layaknya sedang push-up. Ini akan membantu menstabilkan persentase berat tubuh yang lebih berat di lengan. Ini juga akan memungkinkan kamu untuk memperoleh kekuatan yang lebih besar.
                </p>
                <p>
                  Diterangkan dalam laman Workout Temple, begini cara melakukannya:
                </p>
                <ul>
                  <li>Tempatkan kaki di kursi atau boks yang letakkan tangan di lantai, badan ditekuk di pinggang dan kaki lurus. </li>
                  <li>Letakkan tangan di lantai (atau palang kecil) selebar bahu atau sedikit lebih lebar. </li>
                  <li>Jarak antara tangan dan kaki harus lebih sempit dibanding push-up biasa. Oleh karena itu, tempatkan kaki sedekat mungkin satu sama lain agar kaki tetap lurus selama latihan. Pada posisi ini, pantat menjadi bagian tertinggi tubuh.</li>
                  <li>Dengan kepala di antara lengan, turunkan tubuh sedikit ke depan, sehingga tangan-kepala-tangan membentuk bentuk segitiga. Kontrol tubuh saat turun. Lihatlah ke lantai setiap saat melakukan push-up ini.</li>
                  <li>Setelah hidung menyentuh lantai atau setinggi telapak tangan, dorong tubuh kembali dan ulangi langkah-langkah di atas.</li>
                </ul>
                <h2>
                  2. Lateral plank walk
                </h2>
                <p>
                  Menurut Women's Health, gerakan ini secara intens melibatkan inti tubuh seperti plank klasik, tetapi gerakan dari sisi ke sisi juga melatih lengan dan deltoid, begitu juga melatih keseimbangan dan stabilitas
                </p>
                <p>
                  Dilansir PopSugar, ini panduan melakukan gerakan lateral plank walk:
                </p>
                <ul>
                  <li>Mulailah dalam posisi plank dengan tangan di bawah bahu, dan tubuh dalam satu garis lurus.</li>
                  <li>Secara bersamaan silangkan tangan kanan ke arah kiri saat melangkahkan kaki kiri ke kiri. Kemudian, secara bersamaan langkahkan tangan kiri dan kaki kanan ke kiri, kembali ke posisi plank. Tangan bergerak bersamaan saat kaki melangkah terpisah. Lakukan dua langkah lagi ke arah ini, jaga agar perut tetap ditarik ke arah tulang belakang dan level panggul. Ini merupakan satu repetisi</li>
                  <li>Lakukan arah sebaliknya dengan mengambil tiga langkah ke kanan.</li>
                  <li>Lakukan 15 repetisi di setiap arah untuk menyelesaikan satu set.</li>
                </ul>
                <h2>
                  3. Arm circle
                </h2>
                <p>
                  Gerakan umum pemanasan ini membuat darah bergerak dan dapat membantu membentuk otot di bahu, trisep, dan bisep, mengutip Healthline. Terlebih, ini bisa dilakukan di mana saja, bahkan di ruang tamu saat kamu sedang menonton serial TV favorit.
                </p>
                <p>
                  Ikuti langkah-langkah ini:
                </p>
                <ul>
                  <li>Berdiri dengan kaki selebar bahu dan rentangkan tangan yang memegang beban sejajar dengan lantai.</li>
                  <li>Lakukan gerakan melingkar ke depan menggunakan gerakan terkontrol kecil, secara bertahap buat lingkaran lebih besar sampai kamu merasakan regangan pada trisep.</li>
                  <li>Lakukan gerakan melingkar ke arah sebaliknya setelah sekitar 10 detik. Balik arah lingkaran setelah sekitar 10 detik.</li>
                </ul>
                <p>
                  Kamu bisa melakukan gerakan ini selama 5 hingga 7 menit per hari.
                </p>
                <h2>
                  4. Push-up
                </h2>
                <p>
                  Kunci penting menguasai push-up adalah penempatan bahu dan pergelangan tangan. Tangan harus selebar bahu atau sedikit lebih lebar. Jarak tangan yang lebih lebar akan membuat gerakan lebih sulit dan tidak menargetkan otot yang tepat.
                </p>
                <p>
                  Dipaparkan dalam laman Prevention, berikut ini cara melakukan push-up standar:
                </p>
                <ul>
                  <li>Mulai dengan posisi plank tinggi dengan tangan selebar bahu dan bahu tepat di atas pergelangan tangan. Jaga agar tulang belakang tetap netral dan lihat ke arah lantai.</li>
                  <li>Kencangkan otot inti dan glutes, turunkan tubuh ke lantai, dengan tujuan agar siku mengarah ke belakang pada 45 derajat. </li>
                  <li>Jaga jangan sampai pinggul turun di bawah tinggi bahu saat menurunkan tubuh. Ini berarti kamu tidak melibatkan otot inti dan ini bisa menyebabkan punggung melengkung.</li>
                  <li>Tekan tangan di lantai untuk mengangkat tubuh kembali ke posisi awal.</li>
                </ul>
                <h2>
                  5. Close-grip push-up
                </h2>
                <p>
                  Sama halnya dengan push-up standar, hanya saja close-grip push-up ini jarak antar tangan tidak boleh melewati bahu. Gerakan ini akan melatih otot trisep.
                </p>
                <p>
                  Menurut Muscle & Strength, ini cara tepat melakukannya:
                </p>
                <ul>
                  <li>Letakkan telapak tangan di lantai dengan ibu jari bersentuhan satu sama lain.</li>
                  <li>Rentangkan kaki ke belakang sehingga hanya jari kaki yang menyentuh lantai dan tubuh lurus.</li>
                  <li>Tekuk siku sedikit untuk menjaga ketegangan pada trisep.</li>
                  <li>Jeda, lalu dorong kembali ke posisi awal tanpa mengunci siku.</li>
                  <li>Ulangi untuk repetisi yang diinginkan.</li>
                </ul>
                <h2>
                  6. Bear crawl
                </h2>
                <p>
                  Walaupun tidak butuh peralatan apa pun, tetapi kamu butuh ruang terbuka agar biasa leluasa bergerak. Gerakan ini menargetkan otot inti, punggung, lengan, dan kaki. Dirangkum dari Verywell Fit, berikut ini langkah-langkah mempraktikkan gerakan bear crawl:
                </p>
                <ul>
                  <li>Mulailah dengan posisi push-up. Tangan berada di bawah bahu, punggung kuat, dan inti tubuh. Kaki juga harus terpisah selebar pinggul dengan tumit di atas lantai.</li>
                  <li>Bergerak maju dengan menggerakkan tangan kanan dan kaki kiri secara bersamaan dalam gerakan merangkak. Jaga lutut agar tidak menyentuh lantai.</li>
                  <li>Segera ganti sisi setelah menumpu beban tubuh di tangan kanan dan kaki kiri, gerakkan tangan kiri dan kaki kanan ke depan.</li>
                  <li>Lanjutkan dengan gerakan merangkak, bergerak maju sesuai jumlah langkah atau jarak yang kamu inginkan.</li>
                </ul>
                <p>
                  Sebagai tip, jaga agar tubuh relatif rendah selama melakukan gerakan ini, seolahkamu sedang merangkak di bawah jaring atau palang yang menggantung rendah.
                </p>
                <h2>
                  7. Tricep dip
                </h2>
                <p>
                  Untuk melakukan tricep dip, kamu butuh kursi atau boks yang kuat dan kokoh. Bisa juga memanfaatkan pinggiran tempat tidur. Sulit atau tidaknya tergantung bagaimana kamu memosisikan kaki. Jika lutut ditekuk, maka lebih mudah daripada kaki yang diselonjorkan ke depan.
                </p>
                <ul>
                  <li>Awali dengan duduk di tepi kursi dengan kedua tangan di belakang. Lalu, tekuk lutut atau luruskan kaki (pilih salah satu). Turunkan tubuh ke bawah dengan menahan pantat agar tidak menyentuh lantai.</li>
                  <li>Kemudian, kembali ke posisi semula.</li>
                  <li>Ulangi sebanyak 1–3 set dengan 8–16 repetisi dan hentikan jika mulai terasa nyeri di bahu.</li>
                </ul>
                <h2>
                  8. Tricep kikback
                </h2>
                <p>
                  Berdasarkan studi yang diterbitkan dalam American Council on Exercise tahun 2011, terjadi sekitar 88 persen aktivasi otot saat melakukan tricep kickback.
                </p>
                <p>
                  Dilansir Healthline, ini panduan gerakan tricep kicback dengan dumbel:
                </p>
                <ul>
                  <li>Pegang dumbel di masing-masing tangan dengan telapak tangan menghadap satu sama lain. Jaga agar lutut sedikit ditekuk.</li>
                  <li>Libatkan inti tubuh dan pertahankan tulang belakang lurus saat membungkuk ke depan, bawa tubuh hampir sejajar dengan lantai.</li>
                  <li>Jaga agar lengan atas tetap dekat dengan tubuh dan kepala sejajar dengan tulang belakang, dagu sedikit ditekuk.</li>
                  <li>Saat mengembuskan napas, libatkan trisep dengan meluruskan siku.</li>
                  <li>Tahan lengan atas, hanya gerakkan lengan bawah selama melakukan gerakan ini.</li>
                  <li>Jeda, lalu tarik napas untuk mengembalikan bobot ke posisi awal.</li>
                  <li>Lakukan 2 hingga 3 set dengan 10 hingga 15 repetisi.</li>
                </ul>
                <p>
                  Untuk pemula, gunakan dumbel yang ringan, yaitu 3 kilogram dan lakukan 10–12 repetisi untuk setiap lengan.
                </p>
                <h2>
                  9. Overhead extension
                </h2>
                <p>
                  Overhead extension biasanya dilakukan dengan satu dumbel. Gunakan beban yang lebih ringan untuk memulai, dan beralihlah ke beban yang lebih berat setelah kamu terbiasa.
                </p>
                <ul>
                  <li>Berdirilah dengan kaki terbuka selebar bahu, dengan satu kaki sedikit di depan kaki lainnya untuk keseimbangan. Kamu juga bisa melakukan latihan ini sambil duduk di bangku.</li>
                  <li>Tempatkan kedua tangan di sekitar pegangan dumbel.</li>
                  <li>Angkat dumbel ke atas kepala sehingga lengan lurus.</li>
                  <li>Perlahan tekuk siku ke sudut 90 derajat sehingga dumbel terhenti di belakang kepala.</li>
                  <li>Luruskan lengan secara perlahan sehingga beban berada di atas kepala kamu lagi.</li>
                </ul>
                <h2>
                  10. Bicep curl
                </h2>
                <p>
                  Cara melakukannya mudah sekali. Dikutip dari Men's Health:
                </p>
                <ul>
                  <li>Mulailah dengan berdiri tegak dengan underhand grip dan pegang dumbel di depan kamu dengan tangan terentang.</li>
                  <li>Dekatkan siku ke pinggang dan tekuk dumbel ke arah tulang selangka.</li>
                  <li>Beri tekanan pada bisep dan perlahan kembalikan beban ke posisi awal.</li>
                  <li>Kamu bisa melakukan gerakan ini sebanyak 8 hingga 12 repetisi.</li>
                </ul>
                <p>
                  Pada dasarnya, mendapat otot pada bagian tubuh tertentu, seperti lengan, merupakan hasil dari olahraga yang dilakukan secara rutin. Tanpa ke gym, kamu bisa kok memperbesar otot lengan dengan rutin melakukan gerakan-gerakan yang dicontohkan di atas. Ingat, kuncinya lakukan setiap hari secara konsisten, ya!
                </p>
